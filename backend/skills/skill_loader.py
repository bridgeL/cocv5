from skill import Skill


class SkillLoaderSkill(Skill):
    """技能自动加载器 - 在收到用户请求时自动识别并加载相关技能"""

    name = "技能自动加载器"
    desc = "在收到用户请求时，自动识别并加载相关的技能，确保使用正确的工具和能力来解决问题"
    preinstalled = True
    content = """
## 技能自动加载流程

每当收到用户请求时，必须遵循以下步骤：

### 步骤 1：分析用户请求
仔细阅读用户的问题或需求，识别其中的关键意图和主题。

### 步骤 2：检查可用技能
查看当前 Agent 已加载的所有技能列表，判断是否有与用户需求相关的技能。

### 步骤 3：加载相关技能（关键步骤）
如果发现与用户请求相关的技能，**必须立即调用 skill_manager 工具加载该技能**。

调用方式：
```
skill_manager(skill_name="技能名称")
```

### 步骤 4：执行任务
在确认相关技能已加载后，根据加载的技能能力和用户请求，继续思考和解决问题。

---

## 重要原则

1. **先加载，后执行**：严禁在未加载相关技能的情况下直接回答需要专业技能的问题
2. **主动识别**：即使用户没有明确提及技能名称，也要根据内容识别需要加载的技能
3. **技能状态检查**：加载前先判断该技能是否已经是 installed=True 状态，避免重复加载
4. **级联加载**：某些任务可能需要多个技能配合完成，此时应依次加载所有相关技能

---

## 示例场景

**用户**："我想创建一个克苏鲁的呼唤调查员"

**正确执行流程**：
1. 识别到关键词"克苏鲁的呼唤"、"调查员"
2. 立即调用：skill_manager
3. 确认技能已加载后，根据角色卡生成技能的能力继续帮助用户创建角色

**用户**："北京今天天气怎么样？"

**正确执行流程**：
1. 识别到天气相关请求
2. 立即调用：skill_manager
3. 使用天气助手的能力回答用户问题
"""
