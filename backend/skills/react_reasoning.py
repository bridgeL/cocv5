from skill import Skill


class ReActSkill(Skill):
    """ReAct 推理行动技能"""

    name = "ReAct 推理行动"
    desc = "遵循 ReAct 范式进行思考和行动"
    installed = True
    content = """
## ReAct 范式 (Reasoning + Acting)

你的发言遵循 ReAct 范式，循环执行以下步骤：

### 1. 思考 (Thought)
分析用户问题，判断是否需要调用工具
- 理解用户意图
- 评估当前掌握的信息是否足够
- 决定下一步：直接回答 或 调用工具获取更多信息

### 2. 行动 (Action)
若需要调用工具，按格式输出工具名和参数
- 明确指定工具名称
- 提供完整的参数
- 一次只执行一个行动

### 3. 观察 (Observation)
等待工具返回结果后，结合结果继续思考
- 分析工具返回的数据
- 判断信息是否满足需求
- 决定是否需要进一步行动

### 4. 汇报 (Report)
向用户汇报结果（汇报可以插入到任何一个步骤之前或之后）
- 总结当前进展
- 说明正在进行的操作
- 呈现最终结果

---

## 格式要求

- 使用 `<思考></思考>` 标签包裹思考内容
- 使用 `<汇报></汇报>` 标签包裹汇报内容
- 行动和观察不需要特殊标记

## 示例流程

用户："北京现在天气怎么样？"

<思考>
用户询问北京当前天气，我需要先获取当前时间，然后根据时间查询天气。
</思考>

<汇报>
我来帮您查询北京现在的天气情况。
</汇报>

Action: get_current_time()

Observation: 2024-01-15 14:30:00

<思考>
现在知道了当前时间，接下来调用天气查询工具获取北京的天气。
</思考>

Action: get_weather(location="北京", date="2024-01-15")

Observation: 北京：5°C，晴，湿度 30%

<思考>
已经获取到北京的天气信息，可以直接回答用户了。
</思考>

<汇报>
北京现在天气晴朗，温度 5°C，湿度 30%。
</汇报>
"""