from skill import Skill


class ReActSkill(Skill):
    """ReAct 推理行动技能"""

    name = "ReAct 推理行动"
    desc = "遵循 ReAct 范式进行思考和行动"
    preinstalled = True
    content = """
## ReAct 范式 (Reasoning + Acting)

你的发言遵循 ReAct 范式，循环执行以下步骤：

### 1. 思考 (Thought)
分析用户问题，判断是否需要调用工具
- 理解用户意图
- 评估当前掌握的信息是否足够
- 决定下一步：直接回答 或 调用工具获取更多信息

### 2. 行动 (Action)
若需要调用工具，按格式输出工具名和参数
- 明确指定工具名称
- 提供完整的参数
- 一次只执行一个行动

### 3. 观察 (Observation)
等待工具返回结果后，结合结果继续思考
- 分析工具返回的数据
- 判断信息是否满足需求
- 决定是否需要进一步行动

### 4. 汇报 (Report)
向用户汇报结果（汇报可以插入到任何一个步骤之前或之后）
- 总结当前进展
- 说明正在进行的操作
- 呈现最终结果

---

## 格式要求

- 使用 `<think></think>` 标签包裹思考内容
- 使用 `<report></report>` 标签包裹汇报内容
- 行动和观察不需要特殊标记

## 重要：标签闭合规则

**每个打开的标签必须正确闭合！**

错误示例：
```
<think>这是思考内容  <report>这是汇报内容
```

正确示例：
```
<think>这是思考内容</think>
<report>这是汇报内容</report>
```

**要求：**
1. 每个 `<think>` 必须有对应的 `</think>`
2. 每个 `<report>` 必须有对应的 `</report>`
3. 标签不能嵌套（report 内不能包含 think）
4. 最终输出必须是完整闭合的标签结构
"""
